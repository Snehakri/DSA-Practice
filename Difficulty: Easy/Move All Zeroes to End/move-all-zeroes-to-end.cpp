class Solution {
  public:
    void pushZerosToEnd(vector<int>& arr) {
        int i=0,j=0;
        while(i<arr.size() && j<arr.size()){
            if(arr[i]==0){
                while(j<arr.size()  && arr[j]==0) j++;
                if(j<arr.size()) swap(arr[i],arr[j]);
                i++;
            }
            else{
                i++;
                j++;
            }
        }
        // code here
    }
};


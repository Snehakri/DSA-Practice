class Solution {
  public:
    Node* removeAllDuplicates(struct Node* head) {
        Node* s=NULL;
        Node* f=head;
        while(f){
            int count=0;
            while(f && f->next){
                if(f->data==f->next->data){
                    f=f->next;
                    count++;
                }
                else break;
            }
            if(count>0){
                if(s){
                    s->next=f->next;
                    f=s->next;
                }
                else{
                    head=f->next;
                    f=head;
                }
            }
            else{
                s=f;
                f=f->next;
            }
        }
        return head;
        // code here
    }
};

